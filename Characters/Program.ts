class VariableAssignment {
    static main(): void {
        // Assigning string values to variables
        let universityName: string = "Software University";
        let programmingMessage: string = "I love programming";

        // Assigning character values to variables
        let charB: string = 'B';
        let charY: string = 'y';
        let charE: string = 'e';

        // Displaying the values of the variables
        console.log("University Name: " + universityName);
        console.log("Character B: " + charB);
        console.log("Character Y: " + charY);
        console.log("Character E: " + charE);
        console.log("Programming Message: " + programmingMessage);
    }
}

// Call the main method to start the program
VariableAssignment.main();
